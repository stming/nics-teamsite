SELECT *
FROM V_ACC_OF_GOLDEN
ORDER BY NIKON_LOCALE, PROD_PROD_ID, PROD_ID

--Accesories
SELECT P.PROD_ID
      ,A.*
FROM   V_PRODUCT P INNER JOIN V_ACC_OF_GOLDEN AS P2A ON P.PROD_ID = P2A.PROD_PROD_ID
       LEFT OUTER JOIN V_PRODUCT AS A ON P2A.PROD_ID = A.PROD_ID 
                                     AND P.NIKON_LOCALE = A.NIKON_LOCALE
WHERE P.NIKON_LOCALE != 'EN_EU' -- EXCLUDE GOLDEN SET
AND   P.NIKON_LOCALE = 'de_CH'
ORDER BY P.NIKON_LOCALE, P.PROD_ID, A.PROD_ID

--V_PROD_RELATED_GOLDEN
SELECT *
FROM V_PROD_RELATED_GOLDEN

--Related Products
-- IF R_PROD_ID IS NULL THEN NO RELATED PRODUCTS FOR THIS PRODUCT
SELECT P.PROD_ID
      ,P.PROD_SHORT_CODE
      ,RP.PROD_ID AS R_PROD_ID
      ,RP.PROD_SHORT_CODE
      ,RP.PATH
FROM   V_PRODUCT P LEFT OUTER JOIN V_PROD_RELATED_GOLDEN AS P2P ON P.PROD_ID = P2P.PROD_PROD_ID
       LEFT OUTER JOIN V_PRODUCT AS RP ON P2P.PROD_ID = RP.PROD_ID AND RP.NIKON_LOCALE = P.NIKON_LOCALE
WHERE P.NIKON_LOCALE != 'en_EU' -- EXCLUDE GOLDEN SET
AND   P.NIKON_LOCALE = 'fr_CH'

--V_PROD_INCLUDED_IN_BOM
SELECT *
FROM V_PROD_INCLUDED_IN_BOM_GOLDEN

-- IF R_PROD_ID IS NULL THEN NO RELATED PRODUCTS FOR THIS PRODUCT
SELECT P.PROD_ID
      ,P.PROD_SHORT_CODE
      ,RP.PROD_ID AS R_PROD_ID
      ,RP.PROD_SHORT_CODE
      ,RP.PATH
FROM   V_PRODUCT P LEFT OUTER JOIN V_PROD_INCLUDED_IN_BOM_GOLDEN AS P2P ON P.PROD_ID = P2P.PROD_PROD_ID
       LEFT OUTER JOIN V_PRODUCT AS RP ON P2P.PROD_ID = RP.PROD_ID AND RP.NIKON_LOCALE = P.NIKON_LOCALE
WHERE P.NIKON_LOCALE != 'en_EU' -- EXCLUDE GOLDEN SET
AND   P.NIKON_LOCALE = 'fr_CH'
ORDER BY PROD_ID, R_PROD_ID

